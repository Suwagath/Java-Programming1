package Basics;

public class MathIsCool {
    public static void main(String[] args) {
        System.out.println(Math.E);
        System.out.println(Math.PI);
        System.out.println(Math.abs(-12));
        System.out.println(Math.acos(.68));
        System.out.println(Math.asin(0.98));
        System.out.println(Math.atan(6));
        System.out.println(Math.cbrt(2));
        System.out.println((int)Math.copySign(8, -89));
        System.out.println(Math.cos(-0.9));
    }
}
